async function parseTypesFile() {
  try {
    const data = await fs.readFile(TYPES_FILE, 'utf-8');
    console.log('‚úÖ Archivo types.ts le√≠do correctamente');

    // Eliminar comentarios inline antes de procesar
    const cleanData = data
      .replace(/\/\/.*$/gm, '')           // Comentarios de l√≠nea
      .replace(/\/\*[\s\S]*?\*\//g, '');  // Comentarios multil√≠nea

    // Validar duplicados
    const modelNames = new Set();
    const enumNames = new Set();

    // Extraer enums
    const enumRegex = /enum\s+(\w+)\s*{([\s\S]*?)}/g;
    let enumMatch;
    while ((enumMatch = enumRegex.exec(cleanData)) !== null) {
      const name = enumMatch[1];
      
      if (enumNames.has(name)) {
        throw new Error(`‚ùå Duplicado: El enum "${name}" ya est√° definido`);
      }
      
      const values = enumMatch[2]
        .split(',')
        .map(v => v.trim())
        .filter(v => v)
        .map(v => v.split('=')[0].trim());
      
      enums.push({ name, values });
      enumNames.add(name);
    }

    // Extraer interfaces y type aliases
    const typeInterfaceRegex = /(interface|type)\s+(\w+)\s*(?:=)?\s*{([\s\S]*?)}/g;
    let match;
    while ((match = typeInterfaceRegex.exec(cleanData)) !== null) {
      const typeName = match[2];
      
      if (modelNames.has(typeName)) {
        throw new Error(`‚ùå Duplicado: El modelo "${typeName}" ya est√° definido`);
      }
      
      const body = match[3];

      if (body.trim()) {
        const fields = body
          .split('\n')
          .map(line => line.trim())
          .filter(line => line && !line.startsWith('}')) // Filtrar llaves
          .map(line => {
            // Validar formato de campo
            if (!/:/.test(line)) {
              console.warn(`‚ö†Ô∏è Formato inv√°lido ignorado en ${typeName}: "${line}"`);
              return null;
            }

            const parts = line.split(':').map(s => s.trim());
            let [name, type] = parts;
            const isOptional = name.endsWith('?');
            const fieldName = isOptional ? name.slice(0, -1) : name;
            const cleanedType = type.replace(/;$/, '').trim();

            return { 
              name: fieldName, 
              type: cleanedType,
              optional: isOptional
            };
          })
          .filter(field => field !== null); // Eliminar campos inv√°lidos

        models.push({ modelName: typeName, fields });
        modelNames.add(typeName);
      }
    }

    console.log(`‚úÖ Modelos encontrados: ${models.map(m => m.modelName).join(', ')}`);
    console.log(`‚úÖ Enums encontrados: ${enums.map(e => e.name).join(', ')}`);
  } catch (error) {
    console.error('‚ùå Error al procesar types.ts:', error.message);
    process.exit(1);
  }
}


